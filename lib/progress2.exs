all = fn(m) ->
  case m do
    [] -> 6
    [v,6] when v != 4 and
               v != 7 and
               v != 8 -> 8
    [4,6] -> 2
    [7,6] -> 0
    [8,6] -> 0
    [7,8,0,6] -> 2
    [v,8,_,6] when v != 7 -> 7
    [v,0,_,6] when v != 3 -> 3
    [7,8,1,6] -> 4
    [7,8,2,6] -> 0
    [7,8,3,6] -> 2
    [0,2,4,6] -> 8
    [1,2,4,6] -> 7
    [3,2,4,6] -> 5
    [5,2,4,6] -> 3
    [7,2,4,6] -> 1
    [8,2,4,6] -> 0
    [7,8,5,6] -> 0
    [3,0,7,6] -> 2
    [3,0,8,6] -> 2
    [0,4,7,8,1,6] -> 2
    [2,4,7,8,1,6] -> 0
    [4,0,7,8,2,6] -> 3
    [8,7,1,2,4,6] -> 0
    [8,5,3,2,4,6] -> 0
    [0,3,5,2,4,6] -> 8
    [0,1,7,2,4,6] -> 8
    [4,0,7,8,5,6] -> 3
    [4,2,7,8,_,6] -> 5
    [5,2,7,8,_,6] -> 4
    [3,0,7,8,_,6] -> 4
    [1,2,3,0,_,6] -> 4
    [4,2,3,0,_,6] -> 1
    [3,0,8,7,1,2,4,6] -> 5
    [1,0,8,5,3,2,4,6] -> 7
    [7,8,0,3,5,2,4,6] -> 1
    # [5,8,0,1,7,2,4,6] -> 3
                    _ -> 3
  end
end
